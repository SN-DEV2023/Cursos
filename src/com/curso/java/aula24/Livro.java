package com.curso.java.aula24;

public class Livro {
	
	
	String livro;
	String título;
	String autores;
	String editora;
	String idioma;
	String genero;
	@Override
	public String toString() {
		return "Livro [livro=" + livro + ", título=" + título + ", autores=" + autores + ", editora=" + editora
				+ ", idioma=" + idioma + ", genero=" + genero + "]";
	}
		
	

}
